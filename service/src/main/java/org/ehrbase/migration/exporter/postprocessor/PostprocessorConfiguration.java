package org.ehrbase.migration.exporter.postprocessor;

import org.springframework.boot.autoconfigure.condition.ConditionalOnProperty;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class PostprocessorConfiguration {

    @Bean
    @ConditionalOnProperty("export.postprocessors.validation.enable")
    public ValidationPostprocessor validationPostprocessor() {
        return new ValidationPostprocessor();
    }

    @Bean
    public EhrStatusPostprocessor ehrStatusPostprocessor() {
        return new EhrStatusPostprocessor();
    }
}
